public class BeastBait {
    private String name;
    private int successModifier;
    private int price;

    public BeastBait(String name, int successModifier, int price) {
        name = name;
        successModifier = successModifier;
        price = price;
    }

    public String getName() {
        return name;
    }

    public int getSuccessModifier() {
        return successModifier;
    }

    public int price() {
        return price;
    }
}

